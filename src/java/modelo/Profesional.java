package modelo;

import java.io.Serializable;
import java.util.Date;

/**
 *
 * @author Andres
 */
public class Profesional implements Serializable{
    private String cedula;
    private String nombre;
    private String direccion;
    private String telefono;
    private Boolean activo = Boolean.TRUE;
    private Date proximaFranja;

    public Profesional() {
    }
    
    

    /**
     * @return the cedula
     */
    public String getCedula() {
        return cedula;
    }

    /**
     * @param cedula the cedula to set
     */
    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the proximaFranja
     */
    public Date getProximaFranja() {
        return proximaFranja;
    }

    /**
     * @param proximaFranja the proximaFranja to set
     */
    public void setProximaFranja(Date proximaFranja) {
        this.proximaFranja = proximaFranja;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the activo
     */
    public Boolean getActivo() {
        return activo;
    }

    /**
     * @param activo the activo to set
     */
    public void setActivo(Boolean activo) {
        this.activo = activo;
    }
}
