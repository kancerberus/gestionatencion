package modelo;

/**
 *
 * @author Andres
 */
public class PuntoL {
    private Integer x;
    private Integer y;

    public PuntoL(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return the x
     */
    public Integer getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(Integer x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public Integer getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(Integer y) {
        this.y = y;
    }
    
}
